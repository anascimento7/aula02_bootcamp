# Faça um programa que peça dois números inteiros e imprima a divisão inteira do primeiro pelo segundo.

numero_um = int(input('Digite o primeiro número: '))
numero_dois = int(input('Digite o segundo número: '))

divisao_inteira = numero_um // numero_dois

print(f"A divisão inteira de {numero_um} por {numero_dois} é de {divisao_inteira}")